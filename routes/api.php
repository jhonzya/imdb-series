Route::middleware('auth:api')->get('/user', function (Request $request) {
    return $request->user();
});

Route::get('search', function (Request $request) {
    return \App\Title::where('primaryTitle', 'like', '%'.$request->get('q').'%')
        ->limit(10)->get();
})->name('search');
